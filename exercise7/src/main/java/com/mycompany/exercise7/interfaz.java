    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        double A, B;
        String resultado;
        try {
            String sA = String.valueOf(txtA.getText());
            String sB = String.valueOf(txtB.getText());
            
            if (sA.isEmpty()||sB.isEmpty()){
                throw new Exception();
            }
        
        A = Double.valueOf(txtA.getText());
        B = Double.valueOf(txtB.getText());
       
        resultado = operaciones.CalcularValor(A, B);
        txtresultado.setText(String.valueOf(resultado));
        
        lblMensaje.setText("");
        lblBlanco.setText("");
        }
        
        catch (NumberFormatException nfe){
          lblMensaje.setText("Ocurri√≥ un problema con el tipo de dato ingresado"); 
          lblBlanco.setText("");
          txtA.setText("");
          txtB.setText("");
          txtresultado.setText("");
        }
        catch (Exception ex){
            lblBlanco.setText("Tiene datos en blanco");
            lblMensaje.setText("");
        }
    }//GEN-LAST:event_btnCalcularActionPerformed

