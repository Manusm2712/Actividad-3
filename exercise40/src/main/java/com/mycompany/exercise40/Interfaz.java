    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        double dato1, dato2, dato3, raiz1, raiz2, raiz3, cuadrado1, cuadrado2, cuadrado3, cubo1, cubo2, cubo3;
        try{
            String sdato1 = String.valueOf(txtdato1.getText());
            String sdato2 = String.valueOf(txtdato2.getText());
            String sdato3 = String.valueOf(txtdato3.getText());
            
            if (sdato1.isEmpty()||sdato2.isEmpty()||sdato3.isEmpty()){
                throw new Exception();
            }
            
        dato1 = Double.valueOf(txtdato1.getText());
        dato2 = Double.valueOf(txtdato2.getText());
        dato3 = Double.valueOf(txtdato3.getText());
        
        raiz1 = Operaciones.CalcularRaiz(dato1);
        txtraiz1.setText(String.valueOf(raiz1));
        raiz2 = Operaciones.CalcularRaiz(dato2);
        txtraiz2.setText(String.valueOf(raiz2));
        raiz3 = Operaciones.CalcularRaiz(dato3);
        txtraiz3.setText(String.valueOf(raiz3));
        
        cuadrado1 = Operaciones.CalcularCuadrado(dato1);
        txtcuadrado1.setText(String.valueOf(cuadrado1));
        cuadrado2 = Operaciones.CalcularCuadrado(dato2);
        txtcuadrado2.setText(String.valueOf(cuadrado2));
        cuadrado3 = Operaciones.CalcularCuadrado(dato3);
        txtcuadrado3.setText(String.valueOf(cuadrado3));
        
        cubo1 = Operaciones.CalcularCubo(dato1);
        txtcubo1.setText(String.valueOf(cubo1));
        cubo2 = Operaciones.CalcularCubo(dato2);
        txtcubo2.setText(String.valueOf(cubo2));
        cubo3 = Operaciones.CalcularCubo(dato3);
        txtcubo3.setText(String.valueOf(cubo3));
        
        lblErrordato.setText("");
        lblBlanco.setText("");
        }
        catch(NumberFormatException nfe){
        lblErrordato.setText("Oucrri√≥ un error con el tipo de dato ingresado");
        lblBlanco.setText("");
        txtcubo1.setText("");
        txtcubo2.setText("");
        txtcubo3.setText("");
        txtcuadrado1.setText("");
        txtcuadrado2.setText("");
        txtcuadrado3.setText("");
        txtraiz1.setText("");
        txtraiz2.setText("");
        txtraiz3.setText("");
        txtdato1.setText("");
        txtdato2.setText("");
        txtdato3.setText("");
        }
        catch(Exception ex){
        lblErrordato.setText("");
        lblBlanco.setText("Todac√≠a hay espacios en blanco");    
        }        
    }//GEN-LAST:event_btnCalcularActionPerformed

